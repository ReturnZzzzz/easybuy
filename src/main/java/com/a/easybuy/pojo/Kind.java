package com.sample;


public class Kind {

  private long id;
  private long fid;
  private String kname;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public long getFid() {
    return fid;
  }

  public void setFid(long fid) {
    this.fid = fid;
  }


  public String getKname() {
    return kname;
  }

  public void setKname(String kname) {
    this.kname = kname;
  }

}
